          <h4>Brookline&reg; Veneer</h4>
<p>Brookline&reg; veneer is the foundation of our entire line of veneer products. As you will see in the pages that follow, Brookline&reg; is produced from a solid laminated block of dyed veneer. When sliced, each block yields 25” x 11’3” or 25” x 8’3” sheets of veneer, depending on the pattern. We recommend using a hot or cold press to bond Brookline&reg; to the proper substrate. (Custom sizes available.)</p>

<h4>Brookline&reg; Paperback Veneers</h4>
<p>Bonded through heat and pressure to a variety of backings, the backed veneers are stress-relieved, sanded and trimmed. The resulting product is an extremely flexible veneer that is ideal for laminating to curved or flat surfaces.</p> 

<h4>Braewood&reg;</h4>
<p>We produce Braewood&reg; by laminating Brookline&reg; veneer to a paper impregnated with phenolic resin. The resulting product can be fabricated with the ease of a decorative laminate and requires only light sanding before finishing.</p> 

<h4>Braewood&reg; Prefinished</h4>
